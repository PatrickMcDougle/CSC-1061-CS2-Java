package edu.csc1061.ch09.student;

public class BirthDate2 {
  private int year;
  private int month;
  private int day;

  public BirthDate2(int year, int newMonth, int newDay) {
    this.year = year;
    month = newMonth;
    day = newDay;
  }

  public void setYear(int newYear) {
    year = newYear;
  }
}
